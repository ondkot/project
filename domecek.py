from turtle import *
a = int(input("Zadej stranu a v pixelech: "))
b = (2 * (a ** 2)) ** 0.5
c = ((a ** 2) / 2) ** 0.5
shape("turtle")
penup()
forward(a)
left(135)
pendown()
color("#800000")
forward(b)
left(225)
forward(a)
left(225)
forward(b)
left(225)
forward(a)
color("red")
left(315)
forward(c)
left(270)
forward(c)
left(315)
color("#800000")
forward(a)
left(270)
forward(a)

done()
